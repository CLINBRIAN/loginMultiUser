    public Main(String username, String role) {
        initComponents();
        this.uname = username;
        this.role = role;

        lblRole.setText(role);
        String utype = lblRole.getText().trim();

        if (utype.equals("Admin")) {
            lblUser.setText(this.uname);
            lblUser.setForeground(Color.red);

            lblRole.setText(this.role);
            lblRole.setForeground(Color.red);
        } else if (utype.equals("Engineer")) {
            lblUser.setText(this.uname);
            lblUser.setForeground(Color.blue);

            lblRole.setText(this.role);
            lblRole.setForeground(Color.blue);

        } else {
            lblUser.setText(this.uname);
            lblUser.setForeground(Color.green);

            lblRole.setText(this.role);
            lblRole.setForeground(Color.green);

        }

    }

